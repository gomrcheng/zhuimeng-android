package com.chat.wkredpacket.model;

public class RedOneBean {
    private String uid;
    private String uname;
    private String fee;
    private String save_date;
    private boolean is_luck;

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getUname() {
        return uname;
    }

    public void setUname(String uname) {
        this.uname = uname;
    }

    public String getFee() {
        return fee;
    }

    public void setFee(String fee) {
        this.fee = fee;
    }

    public String getSave_date() {
        return save_date;
    }

    public void setSave_date(String save_date) {
        this.save_date = save_date;
    }

    public boolean isIs_luck() {
        return is_luck;
    }

    public void setIs_luck(boolean is_luck) {
        this.is_luck = is_luck;
    }
}
